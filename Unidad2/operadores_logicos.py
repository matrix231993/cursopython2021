#Operadores Logicos

# not  :  Negacion Logica

bandera = not True
print(bandera)

bandera= not False
print(bandera)


# Conjuncion :  And  :  Tabla de Verdad

bandera = True and True   # Verdadera
bandera = True and False
bandera = False and True
bandera = False and False

print(bandera)


# Dsyuncion :  OR  :   Tabla de Verdad

bandera = True and True   # Verdadera
bandera = True and False  # Verdadera
bandera = False and True  # Verdadera
bandera = False and False  # False

print(bandera)



   
    

 