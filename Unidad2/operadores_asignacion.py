#Operadore de Asignacion

# definicion de una variable
variable_1 = 20

#Suma y asigna
variable_1 += 5
print(type(variable_1))
print(variable_1)   #25

#Resta y asigna
variable_1 -= 10
print(type(variable_1))
print(variable_1)   #15

#Multiplicacion y asigna
variable_1 *= 10
print(type(variable_1))
print(variable_1)   #150

#Divide y asigna     #  (Operacion basica)
variable_1 /= 10
print(type(variable_1))
print(variable_1)   #15.0 float

     
variable_2 = 110       #  (Operacion de tipo de dato entero)
variable_2 //= 10
print(type(variable_2))
print(variable_2)   # int

#Resta 
variable_2 = 6     #  (Operacion de tipo de dato entero)
variable_2 %= 2
print(type(variable_2))
print(variable_2)   # int

#Potencia
variable_2 = 5       #  (Operacion de tipo de dato entero)
variable_2 **= 2
print(type(variable_2))
print(variable_2)   # int





