# Bool (Boolean)  :  true  y false
# casting =     0 false  y 1 true

#Igual que :   representa  "=="
igual_que = "4" == 2
print(type(igual_que))
print(igual_que)

#Distinto que :   representa   "!="
distinto_que = "42" !=  "41"
print(type(distinto_que))
print(distinto_que)

#Mayor que :   representa   ">"
mayor_que = 40 >  41
print(type(mayor_que))
print(mayor_que)

#menor que :   representa   "<"
menor_que = 40 <  38
print(type(menor_que))
print(menor_que)

#Mayor igual que :   representa   ">="
mayor_igualque = 41 >=  41
print(type(mayor_igualque))
print(mayor_igualque)

#menor igual que :   representa   "<="
menor_igualque = 40 <=  41
print(type(menor_igualque))
print(menor_igualque)



#menor que :   representa   "!="
menor_que = 40 <=  40
print(type(menor_que))
print(menor_que)