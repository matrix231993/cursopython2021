# Verificar notas 
# Rango :    0  a 5  : perdiste el año
# Rango :    6  a 7  : Supletorio
# Rango :    8  : Bueno
# Rango :    9  : Muy Bueno
# Rango :    10 : Sobresalientes

nota_final = float (input("Ingresa la nota de tu examen : \n"))

if nota_final >= 0 and nota_final <= 5:
    print("Perdiste de Año")
elif nota_final >=6 and nota_final <=7:
    print("Tevas a supletorio")
elif nota_final == 8:
    print("bueno")
elif nota_final == 9:
    print("My bueno")
elif nota_final == 10:
    print("Sobresaliente")
else:
    print("La opcion enviada no exixte!!!!Lo siento")



